                    	<p>Select how often the procedure should be performed. This is important because as soon as the procedure has steps and it is linked to a machine it must be done. If it is not it will show up as overdue on the maintenance reports. If it is not preventative maintenance you can choose for it to be "Emergency", "Special", and "Other." It will not be required regularly but can be done as needed.</p>
